    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String FN = tFN.getText();
        Random random = new Random();
        
        // Generating a random 4-digit number
        int code = random.nextInt(9000) + 1000; // Range: 1000 to 9999
        try {
            p = con.prepareStatement("INSERT INTO FAM_USER (FCODE, FAMNAME) VALUES (?, ?)");
            p.setInt(1, code);
            p.setString(2, FN);
            p.executeUpdate();
            
            
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null,e);
        }
        
        jPanelCODE.setVisible(true);
        String sCODE = Integer.toString(code); // Convert int to String
        labelCODE.setText(sCODE);
        
    }//GEN-LAST:event_jButton3ActionPerformed

